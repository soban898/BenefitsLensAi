import { useState } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import {
  Copy,
  Check,
  FileDown,
  Loader2,
} from 'lucide-react';
import type { BenefitsAnalysis } from '../types';

interface ExportBarProps {
  analysis: BenefitsAnalysis;
  fileName: string;
}

function buildSummaryText(analysis: BenefitsAnalysis, fileName: string): string {
  const lines: string[] = [];
  lines.push('BENEFITLENS AI — DOCUMENT ANALYSIS SUMMARY');
  lines.push('==========================================');
  lines.push(`File: ${fileName}`);
  lines.push(`Document Type: ${analysis.documentType}`);
  lines.push(`Plan Type: ${analysis.planType}`);
  lines.push('');
  lines.push('SUMMARY');
  lines.push('-------');
  lines.push(analysis.summary);
  lines.push('');

  const sections: [string, string[]][] = [
    ['KEY DEADLINES', analysis.keyDeadlines],
    ['IMPORTANT DATES', analysis.importantDates],
    ['COVERAGE', analysis.coverage],
    ['EMPLOYEE RESPONSIBILITIES', analysis.employeeResponsibilities],
    ['EMPLOYER RESPONSIBILITIES', analysis.employerResponsibilities],
    ['IMPORTANT WARNINGS', analysis.importantWarnings],
    ['NEXT STEPS', analysis.nextSteps],
  ];

  for (const [title, items] of sections) {
    lines.push(title);
    lines.push('-'.repeat(title.length));
    if (items.length === 0) {
      lines.push('  None identified.');
    } else {
      items.forEach((item, i) => lines.push(`  ${i + 1}. ${item}`));
    }
    lines.push('');
  }

  if (analysis.commonQuestions.length > 0) {
    lines.push('COMMON QUESTIONS');
    lines.push('----------------');
    analysis.commonQuestions.forEach((qa, i) => {
      lines.push(`  Q${i + 1}: ${qa.question}`);
      lines.push(`  A: ${qa.answer}`);
      lines.push('');
    });
  }

  lines.push('==========================================');
  lines.push('Generated by BenefitLens AI');
  return lines.join('\n');
}

